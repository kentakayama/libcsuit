int main(void)
{
    int exit_code = EXIT_FAILURE;
    suit_processor_context_t *processor_context = NULL;
    if ((processor_context = malloc(sizeof(suit_processor_context_t) + 300)) == NULL) {
        goto out;
    }

    UsefulBuf manifest;
    suit_processor_init(processor_context, 300, NULL, &manifest);
    manifest.len = ______testfiles_suit_manifest_exp0_suit_len;
    memcpy(manifest.ptr, ______testfiles_suit_manifest_exp0_suit, ______testfiles_suit_manifest_exp0_suit_len);
    suit_process_flag_t process_flags;
    process_flags.all = UINT16_MAX;
    process_flags.uninstall = 0;

    if (suit_processor_add_manifest(processor_context, UsefulBuf_Const(manifest), process_flags) != SUIT_SUCCESS) {
        goto out;
    }
    if (suit_processor_add_recipient_key(processor_context, CBOR_TAG_COSE_SIGN1, T_COSE_ALGORITHM_ESP256, trust_anchor_esp256_cose_key_public) != SUIT_SUCCESS) {
        goto out;
    }
    if (suit_process_envelope(processor_context) != SUIT_SUCCESS) {
        goto out;
    }
    exit_code = EXIT_SUCCESS;

out:
    suit_processor_free(processor_context);
    free(processor_context);

    return exit_code;
}
